package com.stockops.Market;

/**
 *
 * @author mridulregmi
 */
public class Commodity {
    private String name;
    private String symbol;
    private float price;
    private String unit;
    public Commodity(){
        
    }
    public Commodity(String name, String symbol, float price){
        this.name=name;
        this.symbol=symbol;
        this.price=price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }
    
}
